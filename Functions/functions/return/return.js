// Function with return:

// When you use a function with a return statement, 
// it specifically gives you something back as a result that you can use or save for later.
// Example:

function add(a, b) {
    return a + b;
  }
  
  const sum = add(5, 3);
  console.log(sum); // Outputs: 8

//   does the same addition operation without using the return statement explicitly:

function add(a, b) {
    const sum = a + b;
    console.log(sum);
  }
  
  add(5, 3); // Outputs: 8
  

 